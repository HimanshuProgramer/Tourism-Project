  <h2 class="infoh2">18.  Pandav Caves – Mythological Site</h2>
  <img  src="images/Pandav Gufa.jpg" alt="Pandav Gufa.jpg" class="infoimg">
  <p class="infopara" >Pandav Caves are located in Pipariya and features astounding sights of
  impeccable nature. The caves are believed to be resided by the Pandavas during their period 
  of exile. The caves are frequently visited by travelers interested in unveiling the facts 
  about mythological places. The place is also loved by archaeologists wandering in Madhya Pradesh. 
  Pandav Caves are a group of five rock-cut temples that served as the abode of the Pandavas 
  during their exile period. Another story is that Buddhist monks later used the caves for
  meditation. Those interested in mythology should visit this incredible natural attraction.
  </p>
  <ul class="info_ul" >
		<li type="disc"><b>Best Time to Visit – </b>October to June</li>
		<li type="disc"><b>Ideal Duration – </b>1 Day</li>
