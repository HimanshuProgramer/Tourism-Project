  <h2 class="infoh2">19. Bhimbetka Rock Shelters – A Historical Site</h2>
  <img  src="images/Rock-Shelters-of-Bhimbetka.jpg" alt="Rock-Shelters-of-Bhimbetka.jpg" class="infoimg">
  <p class="infopara" >Bhimbetka Rock Shelters is a world-famous archaeological site and is located
  in the Raisen District in Madhya Pradesh. It has around 750 rock shelters and 7 hills that you 
  must have a look at. An eminent UNESCO world heritage site, Bhimbetka Caves or Rock Shelters will 
  take you back to the paleolithic and mesolithic period ,and also the beginning of the South Asian 
  Stone Age. The caves are frequented by history buffs interested in the earliest traces of human 
  life on the Indian subcontinent. You can see many paintings in the caves, the oldest dating back
  to over 30,000 years. The caves are carved with paintings that depict the existence of dance and 
  art during the Paleolithic period. These rock shelters are one of the most popular places to 
  visit in Madhya Pradesh for all history buffs.
  </p>
  <ul class="info_ul" >
		<li type="disc"><b>Best Time to Visit – </b>October to March</li>
		<li type="disc"><b>Ideal Duration – </b>1 Day</li>
		<li type="disc"><b>Key Attractions – </b>The carved paintings on the cave walls and the 
		750 rock shelters that you must explore.</li>
