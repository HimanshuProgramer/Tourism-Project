	<h2 style="color:blue;padding-left:35px;">Madhya Pradesh Tourism Guide</h2><br>
	<h2 style="color:blue;padding-left:40px;">About</h2><br>
	
	<p style="text-align:justify;word-spacing:7px;margin-left:80px;margin-right:80px;padding:10px;"><i>
	The Heart of Incredible India, Madhya Pradesh is perhaps the only destination that can live up to
	this title that’s awarded to it. With the perfusion of everything that India is known for, rich 
	heritage, deep cultural roots, intriguing history, astounding landscape and vast array of wildlife, 
	the state is truly the ideal travel destination for any passionate traveller. From the erotic carvings
	of Khajuraho temples and the grand architecture of Mandu to the sublime jungles of Kanha or 
	Bandhavgarh with their majestic beasts and beautiful sunsets of Pachmarhi, you are in for a 
	delightful treat with Madhya Pradesh tourism. <br><br>
	
	This centrally located state is named based on its geographical location within the country; with 
	‘Madhya’ meaning ‘Centre’. But what was only a starkly literal manifestation, came out to be known 
	as a gratifying truth about the state. With MP being at nothing less than the centre of India’s
	exalting art, culture, religion and history, tourism in Madhya Pradesh resonates strongly with those
	looking to explore the nation pedantically. While the state is filled with innumerable ancient 
	monuments, it is also progressive with modern amenities and developments.<br><br>
	
	The food, cultural practices and native people of the state are also something that can only be 
	described as impressive. Because of such dynamism and vibrancy of MP is why its fame and demand as a
	tourist destination has transcended geographical boundaries at such an amazing rate. <br><br>
	
	It hasn’t been long since Madhya Pradesh found its rightful place as a tourist destination. Often
	underrated as one, the state is in fact a treasure trove of resplendent sites to visit, for both 
	their striking natural beauty and admirable monuments. The region is filled with places like Orccha,
	Jabalpur, Burhanpur, Amarkantak, Pachmarhi and Bhimbetka which are infinitely blessed with unsullied
	natural surroundings and which do not refrain from leaving you spellbound with their powerful charm.<br><br>

	While at the same time, Madhya Pradesh tourism also offers you many wonderful places that are well
	known for their unparalleled mysticism that emanates from their purely imperial monuments like 
	temples, palaces and forts that demand innate respect. Boasting of such exemplars, the region has 
	Mandu which has a number of skilfully shaped palaces and forts, Gwalior with its gargantuan fort 
	that houses several ornate temples and palaces, and Khajuraho with its unique and simply ravishing 
	temples that portray masterly carved erotic sagas.	<br> <br>
	
	Ancient temples and pilgrimage sites in the state are another reason why tourism in Madhya Pradesh 
	has gained such boosting response from travellers both national and global. You will find some
	noteworthy examples of these at Ujjain with the widely famous Mahakaleshwar temple, Amarkantak with
	its pious ghats and many heritage temples, Sanchi with its well-known Buddhist Stupa which
	inadvertently ignites spirituality within and Orccha, Omkareshwar, and Maheshwar with some more 
	intricately carved old temples that radiate peace and harmony. <br><br>
	
	There are still many other wonderful places to visit in MP with the likes of Bhopal, Indore, 
	Shivpuri, Bhimbetka etc. that are a blend of old world charm and new age developments that wins 
	hearts. <br><br>
	
	Nevertheless, tourism in MP is not just about sightseeing. The state being profusely opulent in 
	terms of wildlife and intimidating verdant jungles, offers several exciting nature parks, animal
	sanctuaries and tiger reserves that any nature lover simply cannot fathom to miss. Pench, Kanha 
	National Park, Bandhavgarh, Satpura and Panna are such places which have abundance of flora and 
	fauna in their natural habitat. You can enjoy watching them and many beautiful birds as well through 
	jeep safaris organized in these parks. <br><br>
	
	However, even safaris doesn’t cover the long list of tourism offerings in this beautiful Indian 
	state. You can very well enjoy a range of exciting and adrenaline rushing activities from the likes 
	of river rafting, water zorbing, zip lining, hot air ballooning, museums hopping, cave explorations, 
	rock climbing and what not, right here in Madhya Pradesh. In short, MP is a place for every kind of
	traveller, those with a knack to witness the bygones days, those with a penchant to be in close 
	proximity with undisturbed nature and those too who do not believe in simply watching but in putting
	their energy to use with myriad fun activities. The list of things to do here is never going to be
	covered in a single trip to the region, you are always welcome to keep coming back for more.<br><br>
	
	
	
